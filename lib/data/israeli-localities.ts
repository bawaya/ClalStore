// De-duplicate by Hebrew name (the primary key for this dataset).
const seen = new Set<string>();
const DEDUPED: IsraeliLocality[] = [];
for (const loc of ISRAELI_LOCALITIES) {
  if (seen.has(loc.name_he)) continue;
  seen.add(loc.name_he);
  DEDUPED.push(loc);
}

export const UNIQUE_ISRAELI_LOCALITIES: readonly IsraeliLocality[] = DEDUPED;

/**
 * Case-insensitive substring match across Hebrew and Arabic names.
 * Returns at most `limit` matches, ordered by (a) name-starts-with
 * beating (b) contains, then alphabetical.
 */
export function searchLocalities(query: string, limit = 20): IsraeliLocality[] {
  const q = query.trim().toLowerCase();
  if (!q) return [];
  const startsWith: IsraeliLocality[] = [];
  const contains: IsraeliLocality[] = [];
  for (const loc of UNIQUE_ISRAELI_LOCALITIES) {
    const he = loc.name_he.toLowerCase();
    const ar = (loc.name_ar || "").toLowerCase();
    if (he.startsWith(q) || ar.startsWith(q)) {
      startsWith.push(loc);
    } else if (he.includes(q) || ar.includes(q)) {
      contains.push(loc);
    }
    if (startsWith.length + contains.length >= limit * 2) break;
  }
  return [...startsWith, ...contains].slice(0, limit);
}
